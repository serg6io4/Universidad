	protected Boolean incluido(Integer[] value, Set<Integer[]> valoresValidos) {
		Iterator<Integer[]> iterador = valoresValidos.iterator();
		Boolean encontrado = Boolean.FALSE;
		Integer[] solucion;
		while (!encontrado && iterador.hasNext()) {
			solucion = iterador.next();
			if (Arrays.equals(solucion, value)) {
				encontrado = Boolean.TRUE;
			}
		}
		return encontrado;
	}
	
	protected Boolean todosIncluidos(Vector<Integer[]> values, Set<Integer[]> valoresValidos) {
		Boolean result = Boolean.TRUE;
		// comprobamos que todos los valores válidos están incluidos en la solución
		Iterator<Integer[]> iterator = valoresValidos.iterator();
		Integer[] value;
		while(iterator.hasNext() && result) {
			Integer[] solucion = iterator.next();
			Boolean encontrado = Boolean.FALSE;
			int i = 0;
			while (!encontrado && i < values.size()) {
				value = values.get(i);
				if (Arrays.equals(solucion, value)) {
					encontrado = Boolean.TRUE;
				}
				i++;
			}
			result = encontrado;
		}
		return result;
	}
	
